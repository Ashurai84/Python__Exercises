def symmetric_difference_list(lst1, lst2):
    return list(set(lst1) ^ set(lst2))

# Example usage
list1 = [1, 2, 3, 4]
list2 = [3, 4, 5, 6]
print("Symmetric difference of the lists:", symmetric_difference_list(list1, list2))
